// Loops are used to repeat code!

// A while loop will happen WHILE the condition is true.
// This loop will not run.
while (false)
{
    Console.WriteLine("Hello World");
}


// FROM NOW ON: Menus are generally in a do-while with a switch
do
{
    Console.WriteLine("Hello world!");
} while (false);